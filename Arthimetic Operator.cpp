//Atithmetic Operator 
#include<iostream>
using namespace std;
int main()
{
	int a=5,b=3;
	cout<<"Arithmetic Operations :- "<<endl<<endl;
	cout<<"Addition is (+) = "<<a+b<<endl;
	cout<<"Subtraction is (-) = "<<a-b<<endl;
	cout<<"Multiplication is (*) = "<<a*b<<endl;
	cout<<"Division is (/) = "<<(float)a/b<<endl;
    cout<<"Modulus is (%) = "<<a%b<<endl;	
	return 0;
}
